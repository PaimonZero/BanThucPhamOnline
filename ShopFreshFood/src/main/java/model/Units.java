package model;

/**
 *
 * @author nguye
 */
public class Units {
        private int unitId;
        private String name;

    public Units() {
    }

    public Units(int unitId, String name) {
        this.unitId = unitId;
        this.name = name;
    }

    public int getUnitId() {
        return unitId;
    }

    public void setUnitId(int unitId) {
        this.unitId = unitId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Units{" + "unitId=" + unitId + ", name=" + name + '}';
    }
        
        

    
}
